from typing import List
class Solution:
    def numIslands(self, grid: List[List[str]]) -> int:
        m = len(grid)
        n = len(grid[0])
        
        islands = 0
        for i in range(m):
            for j in range(n):
                if grid[i][j] == "1":
                    islands += 1
                    grid[i][j] = "0"
                    stack = [(i,j)]
                    while stack:
                        ic,jc = stack.pop(0)
                        if (ic+1 < m) and grid[ic+1][jc] == "1":
                            grid[ic+1][jc] = "0"
                            stack.append((ic+1,jc))
                        if (ic-1 >= 0) and grid[ic-1][jc] == "1":
                            grid[ic-1][jc] = "0"
                            stack.append((ic-1,jc))
                        if (jc+1 < n) and grid[ic][jc+1] == "1":
                            grid[ic][jc+1] = "0"
                            stack.append((ic,jc+1))
                        if (jc-1 >= 0) and grid[ic][jc-1] == "1":
                            grid[ic][jc-1] = "0"
                            stack.append((ic,jc-1))

        return islands


